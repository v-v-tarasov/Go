// Типы данных. Арифметические операции.

package main

import "fmt"

func main() {
	fmt.Println("5 + 5 =", 5+5)   // +  операция сложения
	fmt.Println("10 - 5 =", 10-5) // -  операция вычитания
	fmt.Println("5 * 5 =", 5*5)   // *  операция умножения
	fmt.Println("10 / 2 =", 10/2) // /  операция деления
	fmt.Println("10 % 2 =", 10%2) // %  остаток от деления
}
